/*
 The Name class contains the users first and last name, as well as setters and getters.
 */
public class Name {
	private String first;
	private String last;
	
	public void setLast(String set) {
		last = set;
		}
	public void setFirst(String set) {
		first = set;
	}
	
	public String getLast() {
		return last;
	}		
	public String getFirst() {
		return first;
	}
}
